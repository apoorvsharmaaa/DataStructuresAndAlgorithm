package com.company;

public class FirstnOddNosWithForLoop {
    public static void main(String[] args) {
        System.out.println("THE FIRST N ODD NUMBERS ARE AS FOLLOW:-");
        int n = 100;
        for(int i =0; i<n; i++){
            i=i+1;
            System.out.println(i);
        }
    }
}
